class BinaryNumber {
	public static void main(String[] args) {
		int number = 14;
		System.out.print(number+"(10) - ");
		System.out.print(number/2/2/2/2/2/2/2%2);
		System.out.print(number/2/2/2/2/2/2%2);
		System.out.print(number/2/2/2/2/2%2);
		System.out.print(number/2/2/2/2%2);
		System.out.print(number/2/2/2%2);
		System.out.print(number/2/2%2);
		System.out.print(number/2%2);
		System.out.print(number%2);
		System.out.print("(2)");						
	}
}		